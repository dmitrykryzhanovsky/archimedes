namespace Archimedes
{
    /// <summary>
    /// Единицы измерения углов.
    /// </summary>
    public enum EAngularUnit
    {
        /// <summary>
        /// Градусы: полный оборот от 0° до 360°.
        /// </summary>
        Deg,

        /// <summary>
        /// Часы: полный оборот от 0h до 24h.
        /// </summary>
        Hour,

        /// <summary>
        /// Радианы: полный оборот от 0 до 2π радиан.
        /// </summary>
        Rad,

        /// <summary>
        /// Доли оборота: полный оборот от 0 до 1 оборота.
        /// </summary>
        Orbit
    }
}