namespace Archimedes
{
    /// <summary>
    /// Знак числа.
    /// </summary>
    public enum ESign
    {
        /// <summary>
        /// Строго отрицательное (знак «минус»).
        /// </summary>
        Negative = -1,

        /// <summary>
        /// Неотрицательное (ноль или положительное).
        /// </summary>
        NonNegative = 0,

        /// <summary>
        /// Строго положительное (знак «плюс»).
        /// </summary>
        Positive = 1
    }
}